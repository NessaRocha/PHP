                <h5>LEI BERENICE PIANA</h5>
                <p>Art. 1º A pessoa com transtorno do espectro autista é considerada pessoa com deficiência, para todos
                    os efeitos legais.
                <p>
                    <br>
                <p>Parágrafo único. Aplicam-se às pessoas com transtorno do espectro autista os direitos e obrigações
                    previstos na Convenção Internacional sobre os Direitos da Pessoa com Deficiência e seu Protocolo
                    Facultativo, promulgados pelo Decreto nº 6.949, de 25 de agosto de 2009, e na legislação pertinente
                    às pessoas com deficiência.
                <p>
                    <br>
                <p>Art. 2º É garantido à pessoa com transtorno do espectro autista o direito à saúde no âmbito do
                    Sistema Único de Saúde – SUS, respeitadas as suas especificidades.
                <p>
                    <br>
                <p>§ 1o Ao Ministério da Saúde compete:
                <p>
                    <br>
                    <br>
                <p>I – promover a qualificação e a articulação das ações e dos serviços da Rede de Atenção à Saúde para
                    assistência à saúde adequada das pessoas com transtorno do espectro autista, para garantir:
                <p>
                    <br>
                    a) o cuidado integral no âmbito da atenção básica, especializada e hospitalar;
                <p>
                    <br>
                    b) a ampliação e o fortalecimento da oferta de serviços de cuidados em saúde bucal das pessoas com
                    espectro autista na atenção básica, especializada e hospitalar;
                <p>
                    <br>
                    c) a qualificação e o fortalecimento da rede de atenção psicossocial e da rede de cuidados de saúde da
                    pessoa com deficiência no atendimento das pessoas com o transtorno do espectro autista, que envolva
                    diagnóstico diferencial, estimulação precoce, habilitação, reabilitação e outros procedimentos definidos
                    pelo projeto terapêutico singular;
                <p>
                    <br>
                    II – garantir a disponibilidade de medicamentos incorporados ao SUS necessários ao tratamento de pessoas
                    com transtorno do espectro autista;
                <p>
                    <br>
                    III – apoiar e promover processos de educação permanente e de qualificação técnica dos profissionais da
                    Rede de Atenção à Saúde quanto ao atendimento das pessoas com o transtorno do espectro autista;
                <p>
                    <br>
                    IV – apoiar pesquisas que visem ao aprimoramento da atenção à saúde e à melhoria da qualidade de vida
                    das pessoas com transtorno do espectro autista;
                <p>
                    <br>
                    V – adotar diretrizes clínicas e terapêuticas com orientações referentes ao cuidado à saúde das pessoas
                    com transtorno do espectro autista, observando suas especificidades de acessibilidade, de comunicação e
                    atendimento.
                <p>
                    <br>
                    § 2º A atenção à saúde à pessoa com transtorno do espectro autista tomará como base a Classificação
                    Internacional de Funcionalidade, Incapacidade e Saúde – CIF e a Classificação Internacional de Doenças –
                    CID-10.
                <p>
                    <br>
                    Art. 3º É garantida proteção social à pessoa com transtorno do espectro autista em situações de
                    vulnerabilidade ou risco social ou pessoal, nos termos da Lei nº 8.742, de 7 de dezembro de 1993.
                <p>
                    <br>
                    Art. 4º É dever do Estado, da família, da comunidade escolar e da sociedade assegurar o direito da
                    pessoa com transtorno do espectro autista à educação, em sistema educacional inclusivo, garantida a
                    transversalidade da educação especial desde a educação infantil até a educação superior.
                <p>
                    <br>
                    § 1º O direito de que trata o caput será assegurado nas políticas de educação, sem discriminação e com
                    base na igualdade de oportunidades, de acordo com os preceitos da Convenção Internacional sobre os
                    Direitos da Pessoa com Deficiência.
                <p>
                    <br>
                <p>§ 2º Caso seja comprovada a necessidade de apoio às atividades de comunicação, interação social,
                    locomoção, alimentação e cuidados pessoais, a instituição de ensino em que a pessoa com transtorno
                    do espectro autista ou com outra deficiência estiver matriculada disponibilizará acompanhante
                    especializado no contexto escolar, nos termos do parágrafo único do art. 3o da Lei no 12.764, de
                    2012.
                <p>
                    <br>
                    Art. 5º Ao tomar conhecimento da recusa de matrícula, o órgão competente ouvirá o gestor escolar e
                    decidirá pela aplicação da multa de que trata o caput do art. 7º da Lei nº 12.764, de 2012.
                <p>
                    <br>
                    § 1º Caberá ao Ministério da Educação a aplicação da multa de que trata o caput, no âmbito dos
                    estabelecimentos de ensino a ele vinculados e das instituições de educação superior privadas, observado
                    o procedimento previsto na Lei no 9.784, de 29 de janeiro de 1999.
                <p>
                    <br>
                    § 2º O Ministério da Educação dará ciência da instauração do processo administrativo para aplicação da
                    multa ao Ministério Público e ao Conselho Nacional dos Direitos da Pessoa com Deficiência – Conade.
                <p>
                    <br>
                    § 3º O valor da multa será calculado tomando-se por base o número de matrículas recusadas pelo gestor,
                    as justificativas apresentadas e a reincidência.
                <p>
                    <br>
                    Art. 6º Qualquer interessado poderá denunciar a recusa da matrícula de estudantes com deficiência ao
                    órgão administrativo competente.
                <p>
                    <br>
                    Art. 7º O órgão público federal que tomar conhecimento da recusa de matrícula de pessoas com deficiência
                    em instituições de ensino vinculadas aos sistemas de ensino estadual, distrital ou municipal deverá
                    comunicar a recusa aos órgãos competentes pelos respectivos sistemas de ensino e ao Ministério Público.
                <p>
                    <br>
                <p>Art. 8º A Secretaria de Direitos Humanos da Presidência da República, juntamente ao Conade, promoverá
                    campanhas de conscientização sobre os direitos das pessoas com transtorno do espectro autista e suas
                    famílias.
                <p>
                    <br>
                <p>Sancionada em 2 de Dezembro de 2014, pela Presidente Dilma Rousseff
